package com.banyuan.homework.syntax;

public class Syntax02 {
    public static void main(String[] args) {
        int i = 17;
        switch (i) {
            case 1:
                System.out.println("i是1");
            case 2:
                System.out.println("i是2");
            case 17:
                System.out.println("i是17");
            default:
                System.out.println("不是以上");

        }
    }
}
